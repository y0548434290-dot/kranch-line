// רשימת המפתחות שכבר יש להם קובץ אודיו אמיתי בימות (מתעדכנת ע"י generate-voice.js
// מתוך התיקייה ivr2:/0/tts). מפתח שלא ברשימה יושמע זמנית כטקסט (TTS של ימות),
// כך שאפשר לפרוס בהדרגה בלי שום שקט — וכל קובץ חדש "משתדרג" אוטומטית בפריסה הבאה.
let MANIFEST = new Set();
try {
    const manifest = require('./voice-manifest.json');
    if (Array.isArray(manifest)) {
        MANIFEST = new Set(manifest);
    }
} catch (_) {
    // אין עדיין manifest — הכל ייפול חזרה לטקסט.
}

// מחזיר הודעת השמעה לפי key: קובץ-אודיו אם הוא קיים בימות, אחרת טקסט (fallback).
// זורק אם ה-key לא מוכר בכלל (לתפיסת שגיאות הקלדה).
function say(key) {
    if (!Object.prototype.hasOwnProperty.call(ALL_LINES, key)) {
        throw new Error(`Unknown voice line key: ${key}`);
    }
    if (MANIFEST.has(key)) {
        return { type: 'file', data: `${VOICE_DIR}/${key}` };
    }
    return { type: 'text', data: ALL_LINES[key] };
}

// הודעת קובץ לעיר לפי מספר הבחירה (1-8).
function cityLine(cityKey) {
    return say(`city_${cityKey}`);
}

// הודעת קובץ לאזור לפי עיר+אזור.
function areaLine(cityKey, areaKey) {
    return say(`area_${cityKey}_${areaKey}`);
}

// הודעת קובץ לכיתה לפי מספר הבחירה (1-12).
function gradeLine(gradeKey) {
    return say(`grade_${gradeKey}`);
}

